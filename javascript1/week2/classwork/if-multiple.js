let a = 5;

let b = 10;

// &&  = and
if (a == 5 && b === '10' ) {
    console.log("a is 5 and b is 10");
} else {
    console.log("Either a is not 5 or b is not 10");
}

// ||  = and
if (a == 5 || b === '10' ) {
    console.log("a is 5 OR b is 10");
} else {
    console.log("a is not 5 AND b is not 10");
}
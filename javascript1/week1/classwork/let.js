var num1 = 10;
console.log(num1);
var num1 = "Florin";
console.log(num1);

/* Example of multiine comment
 ES6 introduced  let as a keyword
 It binds the data type of the variable to the
 data type of the first value assigned to it
*/
let num2 = 10;
console.log(num2);
// num2 was of number type because of "let num2 = 10"
// we cannot re-assign num2 to a different value or
// different value of the same type
let num2 = "Florin"; 
console.log(num2);
// let num2 = 45; // Also not possible


let num1 = 10;
// Read from Right to left
// Assign 9 to num2
let num2 = 9;

// == is the equality operator
console.log(num1 == num2);
console.log(7 == 7);
console.log(num1 == 10);

// == checks only for the value
console.log(num1 == "10");

// === checks for both value and the type
console.log(num1 === "10");

console.log(5<6);

console.log(5 != 6);
console.log(5 !== "5");
console.log(5 !== 5);